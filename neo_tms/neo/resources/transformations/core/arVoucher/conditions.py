"""
Input: isPrintable
output: True or False
"""
def check_condition_for_memo_isPrintable(isPrintable):
    if isPrintable[0] not in [None, ''] :
        return True
    return False

"""
Input: isNonPrintable
output: True or False
"""
def check_condition_for_memo_isNonPrintable(isNonPrintable):
    if isNonPrintable[0] not in [None, ''] :
        return True
    return False